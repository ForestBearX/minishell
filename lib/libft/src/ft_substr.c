#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	s_size;
	char	*result;

	s_size = ft_strlen(s);
	if (start > s_size)
		return (ft_strdup(""));
	if ((s_size - start) < len)
		len = s_size - start;
	result = malloc(sizeof(char) * (len + 1));
	if (result == NULL)
		return (NULL);
	ft_strlcpy(result, s + start, len + 1);
	return (result);
}
